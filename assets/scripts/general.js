$(document).ready(function(){

        //Cards 
        $('.cardTitle').matchHeight();
        $('.imageContainer').matchHeight();
    

    //Initialize Sliders
    $('.searchSlider').slick({
        
        slidesToShow: 1,
        slidesToScroll: 1, 
        arrows:true, 
        dots:true,
        mobileFirst: true,
        responsive:[
            
              {
                breakpoint: 1024,
                settings: {
                    // rows:1,
                    // slidesPerRow:1,
                  slidesToShow: 3,
                  slidesToScroll: 3
                }
              }
        ]
    }
    );
    $('.trendingSlider').slick({
        
        slidesToShow: 1,
        slidesToScroll: 1, 
        arrows:true, 
        dots:true,
        mobileFirst: true,
        responsive:[
              {
                breakpoint: 1024,
                settings: {
                  slidesToShow: 3,
                  slidesToScroll: 3
                }
              }
        ]
    });

    //Add class to slick slider autogenerated row div
     $('.slick-slide').children().addClass('sliderRow');

    //Close Search Modal when clicking 'X'
    $('.closeModal').on('click', function(){
        $('.searchModal').hide();
        if (window.location.href.indexOf('?') > -1) {
            history.pushState('', document.title, window.location.pathname);
        }
    })

    //Show Advanced Search
    $('.advancedButton').on('click', function(){
        $('.advancedSearch').fadeToggle();
        if($('#advancedArrow').hasClass('fa-chevron-down')){
            $('#advancedArrow').switchClass( "fa-chevron-down", "fa-chevron-up", 1000, "easeInOutQuad" );
        }else{
            $('#advancedArrow').switchClass( "fa-chevron-up","fa-chevron-down", 1000, "easeInOutQuad" );

        }
    })

    //Open Image Panel
    $('.cardImage').on('click', function(){
        $url = $(this).attr("data-original");
        $alt = $(this).prop("alt");
        $('.imagePanelImage').prop("src", $url);
        $('.imagePanelImage').prop("alt", $alt);
        $('.imagePanelCopyLink').prop("href", $url);
        $('.imagePanelLink').prop("href", $url);
        $('.imagePanel').css("display", "flex");
    })
    //Copy Image Link on Click
    $('.imagePanelCopyLink').click(function (e) {
        e.preventDefault();
        var copyText = $(this).attr('href');
     
        document.addEventListener('copy', function(e) {
           e.clipboardData.setData('text/plain', copyText);
           e.preventDefault();
        }, true);
     
        document.execCommand('copy');  
      });
    //Close Image Panel
    $('.imagePanelClose').on("click",function(){
        $('.imagePanel').css("display", "none");
    })
   

  


}  
)